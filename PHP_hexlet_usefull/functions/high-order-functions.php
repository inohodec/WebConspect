<?php

//функции высшего порядка

/*
 * Функции высшего порядка — это функции, которые либо принимают, либо возвращают другие функции, 
 * либо делают все сразу. Такие функции, как правило, реализуют некий обобщённый алгоритм 
 * (например, сортировку), а ключевую часть логики делегируют вам через callback-функцию. 
 * Главный плюс от применения таких функций — серьёзное повышение коэффициента повторного 
 * использования кода.
 */

usort($users, fn($a, $b) => $a['age'] <=> $b['age']);